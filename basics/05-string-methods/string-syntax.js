console.log("String Syntax in JavaScript");

// String Syntax
let str1 = "Hello, World!"; // Using double quotes
let str2 = 'Hello, World!'; // Using single quotes
let str3 = `Hello, World!`; // Using backticks (template literals)

console.log(str1); // Output: Hello, World!
console.log(str2); // Output: Hello, World!
console.log(str3); // Output: Hello, World!


// Escaping characters
let str4 = "He said, \"Hello, World!\""; // Using backslash to escape double quotes
console.log(str4); // Output: He said, "Hello, World!"


// Template literals with expressions

let name = "khaled";
console.log(`${name} is a developer`); // Output: khaled is a developer

// /n new line
let str5 = "Hello,\nWorld!";
console.log(str5);
/*
Output:
Hello,
World!
*/

// /t tab
let str6 = "Hello,\tWorld!";
console.log(str6); // Output: Hello,    World!

