/*
    Arithmetic Operators
    + Addition
    - Subtraction
    * Multiplication
    / Division
    % Modulus (Remainder)
    ** Exponentiation
    % Modulus (Remainder)
    ++ Increment
    -- Decrement
*/

// Addition
let a = 10;
let b = "Khaled";

console.log(a + b); // Output: 10Khaled (String concatenation)

// Subtraction
console.log(a - 5); // Output: 5

// Multiplication
console.log(a * 2); // Output: 20

// Division
console.log(a / 2); // Output: 5

// Modulus (Remainder)
console.log(a % 3); // Output: 1

// Exponentiation
console.log(a ** 2); // Output: 100

// Increment
a++;
console.log(a); // Output: 11

// Decrement
a--;
console.log(a); // Output: 10


