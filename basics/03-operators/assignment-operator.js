let a = 10;
a = a + 20;
console.log(a); // 30

let b = 5;
b += 10; // equivalent to b = b + 10
console.log(b); // 15

let c = 8;
c *= 2; // equivalent to c = c * 2
console.log(c); // 16

let d = 20;
d /= 4; // equivalent to d = d / 4
console.log(d); // 5

let e = 15;
e -= 5; // equivalent to e = e - 5
console.log(e); // 10

let f = 3;
f **= 2; // equivalent to f = f ** 2 (3 raised to the power of 2)
console.log(f); // 9   